class UnaryOperation1
{
public static void main(String[] args)
{
int x=10;
System.out.println(".......USING --.......");
System.out.println(x--);
System.out.println(--x);
System.out.println(x-- + x--);
System.out.println(--x + --x);
System.out.println(--x + x--);
System.out.println(x-- + --x);
}
}


