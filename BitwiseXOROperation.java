class BitwiseXOROperation
{
public static void main(String[] args)
{
int x=5;
int y=7;
int z=3;
int result=x^y;
System.out.println("x^y : "+result);
int result1=y^z;
System.out.println("y^z : "+result1);
int result2=x^z;
System.out.println("x^z : "+result2);
int result3=z^y;
System.out.println("z^y : "+result3);
}
}